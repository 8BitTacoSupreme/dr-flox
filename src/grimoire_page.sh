echo -e "${PURPLE}|${NC}                                                              ${PURPLE}|${NC}"
echo -e "${PURPLE}|${NC}  This page was forged in the Declarative Crucible            ${PURPLE}|${NC}"
echo -e "${PURPLE}|${NC}  by the ancient rites of ${CYAN}flox build${NC}.                        ${PURPLE}|${NC}"
echo -e "${PURPLE}|${NC}                                                              ${PURPLE}|${NC}"
echo -e "${PURPLE}|${NC}  Initiate: ${GREEN}${INITIATE}${NC}"
echo -e "${PURPLE}|${NC}                                                              ${PURPLE}|${NC}"
echo -e "${PURPLE}|${NC}  ${YELLOW}\"The artifact is manifest. The transmutation complete.\"${NC}    ${PURPLE}|${NC}"
echo -e "${PURPLE}|${NC}                                                              ${PURPLE}|${NC}"
echo -e "${PURPLE}+==============================================================+${NC}"
echo ""
